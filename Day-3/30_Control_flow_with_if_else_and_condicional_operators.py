# This class is about if, else statement and conditional operators. 

# Example:
# I got my first job at an amusement park, my job was about writing a code that replaced a 
# ticket box, so if a person wanted to go on roller coaster that person needed a height than 
# 120cm

print("Welcome to the rollercoaster !!")
height = int(input("What is your height in cm? "))

if height >= 120:
    print("You can ride the rollercoaster!")
else:
    print("Sorry, you have to grow taller before you can ride")
    
# The if statement is about somenthing is TRUE and else FALSE.

# Comparison Operators:

# As we can see that code above, if we put a height to 120cm the output is on ELSE but if 
# we put >= at the if statement, the result will be "You can ride the rollercoast".

